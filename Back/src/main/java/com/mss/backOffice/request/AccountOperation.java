package com.mss.backOffice.request;

import java.util.Date;

public class AccountOperation {
	
	private Date dco;
	private String lib;
	private String sen;
	private float mon;
	public Date getDco() {
		return dco;
	}
	public void setDco(Date dco) {
		this.dco = dco;
	}
	public String getLib() {
		return lib;
	}
	public void setLib(String lib) {
		this.lib = lib;
	}
	public String getSen() {
		return sen;
	}
	public void setSen(String sen) {
		this.sen = sen;
	}
	public float getMon() {
		return mon;
	}
	public void setMon(float mon) {
		this.mon = mon;
	}

	
	
}
